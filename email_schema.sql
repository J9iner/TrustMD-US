-- Function to update email quota usage
CREATE OR REPLACE FUNCTION update_email_quota_usage()
RETURNS TRIGGER AS $$
BEGIN
    UPDATE tenants 
    SET email_usage = email_usage + 1,
        updated_at = NOW()
    WHERE id = NEW.tenant_id;
    
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger to update email quota usage
CREATE TRIGGER update_email_quota_usage_trigger
    AFTER INSERT ON email_logs
    FOR EACH ROW
    WHEN (NEW.status = 'sent')
    EXECUTE FUNCTION update_email_quota_usage();

-- Function to reset monthly email quota
CREATE OR REPLACE FUNCTION reset_monthly_email_quota()
RETURNS TRIGGER AS $$
BEGIN
    NEW.email_usage = 0;
    NEW.email_quota_reset_date = DATE_TRUNC('month', CURRENT_DATE) + INTERVAL '1 month';
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger to reset monthly email quota
CREATE TRIGGER reset_monthly_email_quota_trigger
    BEFORE UPDATE ON tenants
    FOR EACH ROW
    WHEN (OLD.email_quota_reset_date <= CURRENT_DATE)
    EXECUTE FUNCTION reset_monthly_email_quota();

-- Function to check email quota
CREATE OR REPLACE FUNCTION check_email_quota(p_tenant_id UUID)
RETURNS BOOLEAN AS $$
DECLARE
    quota_limit INTEGER;
    current_usage INTEGER;
    reset_date TIMESTAMP WITH TIME ZONE;
BEGIN
    SELECT email_quota, email_usage, email_quota_reset_date 
    INTO quota_limit, current_usage, reset_date
    FROM tenants 
    WHERE id = p_tenant_id;
    
    -- Reset quota if needed
    IF reset_date <= CURRENT_DATE THEN
        UPDATE tenants 
        SET email_usage = 0,
            email_quota_reset_date = DATE_TRUNC('month', CURRENT_DATE) + INTERVAL '1 month'
        WHERE id = p_tenant_id;
        
        current_usage := 0;
    END IF;
    
    RETURN current_usage < quota_limit;
END;
$$ LANGUAGE plpgsql;

-- Function to get email statistics
CREATE OR REPLACE FUNCTION get_email_statistics(p_tenant_id UUID, p_date_range TEXT DEFAULT '30')
RETURNS JSON AS $$
DECLARE
    result JSON;
BEGIN
    SELECT json_build_object(
        'total_sent', COUNT(*) FILTER (WHERE status = 'sent'),
        'total_failed', COUNT(*) FILTER (WHERE status = 'failed'),
        'total_partial', COUNT(*) FILTER (WHERE status = 'partial'),
        'most_used_type', (
            SELECT type FROM email_logs 
            WHERE tenant_id = p_tenant_id 
            AND created_at >= CURRENT_DATE - INTERVAL '1 ' || p_date_range
            GROUP BY type 
            ORDER BY COUNT(*) DESC 
            LIMIT 1
        ),
        'delivery_rate', ROUND(
            (COUNT(*) FILTER (WHERE status = 'sent')::FLOAT / NULLIF(COUNT(*), 0)) * 100, 2
        ),
        'quota_usage', (
            SELECT json_build_object(
                'used', email_usage,
                'limit', email_quota,
                'remaining', email_quota - email_usage,
                'reset_date', email_quota_reset_date
            )
            FROM tenants WHERE id = p_tenant_id
        )
    )
    INTO result
    FROM email_logs 
    WHERE tenant_id = p_tenant_id 
    AND created_at >= CURRENT_DATE - INTERVAL '1 ' || p_date_range;
    
    RETURN COALESCE(result, '{}'::json);
END;
$$ LANGUAGE plpgsql;

-- View for email analytics
CREATE OR REPLACE VIEW email_analytics AS
SELECT 
    tenant_id,
    DATE_TRUNC('day', created_at) as date,
    type,
    status,
    COUNT(*) as total_emails,
    COUNT(*) FILTER (WHERE status = 'sent') as sent_emails,
    COUNT(*) FILTER (WHERE status = 'failed') as failed_emails,
    COUNT(*) FILTER (WHERE status = 'partial') as partial_emails
FROM email_logs
WHERE created_at >= CURRENT_DATE - INTERVAL '90 days'
GROUP BY tenant_id, DATE_TRUNC('day', created_at), type, status
ORDER BY date DESC;

